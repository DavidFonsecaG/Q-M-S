const functions = require('firebase-functions');
const admin = require('firebase-admin');
admin.initializeApp();

exports.updateTimer = functions.pubsub.schedule('every 1 minutes').onRun((context) => 
{
  const timerRef = admin.database().ref('timer');
  timerRef.set(Date.now()); // Update timer value to current timestamp
  return null;
});

